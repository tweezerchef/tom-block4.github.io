function isEven(num) {
 
// base case #1 if number is 0 return true
if (num === 0){
  return true;
}
// base case 2 if number is 1 return false
  else if (num === 1){
  return false;
  }
// recursive call if number is less then 0 add 2 
  else if (num < 0)
{ return isEven(num + 2)}
  //recursive call the subtracts 2 from the number until number is 1 or 0
      else{
       return isEven(num - 2);
}
}


